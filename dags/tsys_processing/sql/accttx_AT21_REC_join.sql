WITH
  AT00 AS (
  SELECT
    MAST_ACCOUNT_ID,
    AT00_CLIENT_NUM,
    AT00_APPLICATION_NUM,
    AT00_APPLICATION_SUFFIX
  FROM
    `pcb-{env}-processing.domain_account_management.accttx_AT00_REC_EXT` ),
  AT21 AS (
  SELECT
    *
  FROM
    `pcb-{env}-processing.domain_account_management.accttx_AT21_REC_EXT` )
SELECT
  AT00.MAST_ACCOUNT_ID,
  MAST_REC_TYPE,
  AT00.AT00_CLIENT_NUM,
  AT00.AT00_APPLICATION_SUFFIX,
  AT00.AT00_APPLICATION_NUM,
  AT21_SEGMENT_LENGTH,
  AT21_CDATE_STMT_BEG,
  AT21_DATE_STATEMENT,
  AT21_NUM_TRANSACTIONS,
  AT21_NUM_ACTIVE_DISPUTES_OUT,
  AT21_POTENTIAL_FRAUD_TRAN_FLAG,
  AT21_STOP_DELETE,
  AT21_CYCLE_START_DATE,
  IF
  ( AT21_CYCLE_START_TIME = 0, NULL, FORMAT('%02d:%02d:%02d', CAST(AT21_CYCLE_START_TIME / 10000 AS INT64), CAST(TRUNC(MOD(AT21_CYCLE_START_TIME, 10000)/100) AS INT64), MOD(AT21_CYCLE_START_TIME, 100) ) ) AS AT21_CYCLE_START_TIME,
  AT21_CYCLE_END_DATE,
  IF
  ( AT21_CYCLE_END_TIME = 0, NULL, FORMAT('%02d:%02d:%02d', CAST(AT21_CYCLE_END_TIME / 10000 AS INT64), CAST(TRUNC(MOD(AT21_CYCLE_END_TIME, 10000)/100) AS INT64), MOD(AT21_CYCLE_END_TIME, 100) ) ) AS AT21_CYCLE_END_TIME,
  FILE_CREATE_DT
FROM
  AT21
INNER JOIN
  AT00
USING
  (MAST_ACCOUNT_ID,
    AT00_APPLICATION_SUFFIX)