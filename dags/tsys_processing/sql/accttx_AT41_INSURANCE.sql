WITH
  AT00 AS (
  SELECT
    MAST_ACCOUNT_ID,
    AT00_CLIENT_NUM,
    AT00_APPLICATION_NUM,
    AT00_APPLICATION_SUFFIX
  FROM
    `pcb-{env}-processing.domain_account_management.accttx_AT00_REC_EXT` ),
  AT21 AS (
  SELECT
    MAST_ACCOUNT_ID,
    AT00_APPLICATION_SUFFIX,
    AT21_CDATE_STMT_BEG
  FROM
    `pcb-{env}-processing.domain_account_management.accttx_AT21_REC_EXT` ),
  AT41 AS (
  SELECT
    *
  FROM
    `pcb-{env}-processing.domain_account_management.accttx_AT41_INSURANCE_EXT`
  WHERE
    at41_extra_type IN (32) )
SELECT
  MAST_ACCOUNT_ID,
  MAST_REC_TYPE,
  AT00.AT00_CLIENT_NUM,
  AT00.AT00_APPLICATION_NUM,
  AT00_APPLICATION_SUFFIX,
  AT21.AT21_CDATE_STMT_BEG,
  CAST(AT31_DATE_POST AS DATETIME) AS AT31_DATE_POST,
  AT31_TIME_POST,
  AT41_SEGMENT_LENGTH,
  AT41_EXTRA_TYPE,
  AT41_ACCT_TRAN_EXTRA_DATA,
  AT41_INS_RATE_DESC_1,
  AT41_INS_ASSESSED_AMT_1,
  AT41_INS_RATE_DESC_2,
  AT41_INS_ASSESSED_AMT_2,
  AT41_INS_RATE_DESC_3,
  AT41_INS_ASSESSED_AMT_3,
  AT41_INS_RATE_DESC_4,
  AT41_INS_ASSESSED_AMT_4,
  AT41_INS_RATE_DESC_5,
  AT41_INS_ASSESSED_AMT_5,
  AT41_INS_RATE_DESC_6,
  AT41_INS_ASSESSED_AMT_6,
  AT41_INS_RATE_DESC_7,
  AT41_INS_ASSESSED_AMT_7,
  FILE_CREATE_DT
FROM
  AT41
INNER JOIN
  AT21
USING
  (MAST_ACCOUNT_ID,
    AT00_APPLICATION_SUFFIX)
INNER JOIN
  AT00
USING
  (MAST_ACCOUNT_ID,
    AT00_APPLICATION_SUFFIX)