  CAST(GREATEST(
    (NULLIF(SAFE_CAST(TRE_OPEN_TRD AS NUMERIC), -999) - 
     LAG(NULLIF(SAFE_CAST(TRE_OPEN_TRD AS NUMERIC), -999), 1, 0) OVER(
       PARTITION BY MAST_ACCOUNT_ID 
       ORDER BY CAST(FILE_CREATE_DT AS DATE) ASC
     )),
    0
  ) AS STRING) AS MONTHLY_NEWLY_OPENED_TRADES,
  
  -- Audit fields
  CURRENT_DATETIME('America/Toronto') AS REC_LOAD_TIMESTAMP,
  '{dag_id}' AS JOB_ID
