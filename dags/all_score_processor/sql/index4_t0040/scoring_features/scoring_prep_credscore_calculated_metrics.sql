  CAST(SAFE_DIVIDE(
    (SAFE_CAST(AM02_AMT_PAYMENTS_LTD AS NUMERIC) - 
     LAG(SAFE_CAST(AM02_AMT_PAYMENTS_LTD AS NUMERIC), 1, 0) OVER(
       PARTITION BY MAST_ACCOUNT_ID 
       ORDER BY CAST(FILE_CREATE_DT AS DATE) ASC
     )),
    SAFE_CAST(AH01_BALNC_CURR AS NUMERIC)
  ) AS STRING) AS PCB_UTIL,
  
