WITH
  monthly_ranked_data AS (
    SELECT DISTINCT
      a.*, 
      ROW_NUMBER() OVER(
        PARTITION BY a.MAST_ACCOUNT_ID, DATE_TRUNC(CAST(a.FILE_CREATE_DT AS DATE), MONTH) 
        ORDER BY CAST(a.FILE_CREATE_DT AS DATE) DESC, a.REC_LOAD_TIMESTAMP DESC
      ) AS MONTH_RANK
    FROM `pcb-{env}-curated.domain_scoring.CREDSCORE_DAS` a
    WHERE CAST(FILE_CREATE_DT AS DATE) <= CURRENT_DATE()
  ),
  latest_per_month AS (
    SELECT DISTINCT * FROM monthly_ranked_data WHERE MONTH_RANK = 1
  ),
  time_series_with_rank AS (
    SELECT DISTINCT
      *, 
      DENSE_RANK() OVER(
        PARTITION BY MAST_ACCOUNT_ID 
