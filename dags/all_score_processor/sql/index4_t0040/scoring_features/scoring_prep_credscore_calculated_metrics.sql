  CAST(TTE_TRD_EVER_30DAYS_WRS AS STRING) AS TTE_TRD_EVER_30DAYS_WRS,
  CAST(TTE_TRD_EVER_60DAYS AS STRING) AS TTE_TRD_EVER_60DAYS,
  CAST(TTE_TRD_EVER_60DAYS_WRS AS STRING) AS TTE_TRD_EVER_60DAYS_WRS,
  CAST(TTE_TRD_EVER_90_120DAYS AS STRING) AS TTE_TRD_EVER_90_120DAYS,
  CAST(TTE_TRD_EVER_BAD_DEBT AS STRING) AS TTE_TRD_EVER_BAD_DEBT,
  CAST(TTE_TRD_OPENED_PST12_MNTH AS STRING) AS TTE_TRD_OPENED_PST12_MNTH,
  CAST(TTE_TRD_OPENED_PST3_MNTH AS STRING) AS TTE_TRD_OPENED_PST3_MNTH,
  CAST(TTE_TRD_OPENED_PST6_MNTH AS STRING) AS TTE_TRD_OPENED_PST6_MNTH,
  CAST(TTE_TTL_BALNC_OPEN_TRD AS STRING) AS TTE_TTL_BALNC_OPEN_TRD,
  CAST(TTE_TTL_HIGH_CRED_LIM_OPEN_TRD AS STRING) AS TTE_TTL_HIGH_CRED_LIM_OPEN_TRD,
  CAST(TTE_TTL_MONTHLY_TERM_OPEN_TRD AS STRING) AS TTE_TTL_MONTHLY_TERM_OPEN_TRD,
  CAST(TTE_UTIL_PERCENTAGE_OPEN_TRD AS STRING) AS TTE_UTIL_PERCENTAGE_OPEN_TRD,
  CAST(TTE_WORST_CURR_RATING AS STRING) AS TTE_WORST_CURR_RATING,
  CAST(TTE_WORST_RATING_EVER AS STRING) AS TTE_WORST_RATING_EVER,
  
  -- Ranking columns (computed from CTEs)
  CAST(MONTH_RANK AS STRING) AS MONTH_RANK,
  CAST(DATE_RANK AS STRING) AS DATE_RANK,
  
  -- Calculated metric: Amount past due (sum of AH01_AMT_PSTDUE_2 through 12)
  CAST((
    COALESCE(SAFE_CAST(AH01_AMT_PSTDUE_2 AS NUMERIC), 0) + 
    COALESCE(SAFE_CAST(AH01_AMT_PSTDUE_3 AS NUMERIC), 0) +
    COALESCE(SAFE_CAST(AH01_AMT_PSTDUE_4 AS NUMERIC), 0) + 
    COALESCE(SAFE_CAST(AH01_AMT_PSTDUE_5 AS NUMERIC), 0) +
    COALESCE(SAFE_CAST(AH01_AMT_PSTDUE_6 AS NUMERIC), 0) + 
