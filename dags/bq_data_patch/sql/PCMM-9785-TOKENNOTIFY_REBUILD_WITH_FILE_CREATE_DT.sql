(
  ACCT_NBR             STRING,
  TKX_DATE             NUMERIC,
  TKX_TIME             STRING,
  TKX_NAME             STRING,
  EMAIL_ADDR1          STRING,
  EMAIL_ADDR2          STRING,
  ADDR1                STRING,
  ADDR2                STRING,
  CITY                 STRING,
