  TOKEN_ASSURE_LVL     STRING,
  TOKEN_REQUEST_ID     STRING,
  TOKEN_MSG_TYPE       STRING,
  NBR_ACT_ATTEMPTS     NUMERIC,
  TOKEN_EXP_DATE       STRING,
