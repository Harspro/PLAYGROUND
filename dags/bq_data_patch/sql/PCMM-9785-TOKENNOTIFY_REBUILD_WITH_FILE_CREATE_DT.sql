  REC_CHNG_TMS         DATETIME,
  PROCESS_NUM          NUMERIC,
  REC_LOAD_TIMESTAMP   DATETIME
)
PARTITION BY TIMESTAMP_TRUNC(REC_LOAD_TIMESTAMP, MONTH);

INSERT INTO `pcb-{env}-landing.domain_account_management.TOKEN_NOTIFY` (
  ACCT_NBR, TKX_DATE, TKX_TIME, TKX_NAME, EMAIL_ADDR1, EMAIL_ADDR2,
  ADDR1, ADDR2, CITY, STATE_PROV, POSTAL_CODE, COUNTRY_CODE, COUNTY_NAME,
  TOKEN, TOKEN_ASSURE_LVL, TOKEN_REQUEST_ID, TOKEN_MSG_TYPE, NBR_ACT_ATTEMPTS,
  TOKEN_EXP_DATE, TOKEN_EVT_RSN_CODE, TOKEN_EVT_STATUS, TOKEN_TRANS_IND,
  PROVIDER_ID1, PROVIDER_ID2, PROVIDER_ID3, ACCT_ID, STMT_HOLD_CODE,
  LANGUAGE_CODE, RETURN_MAIL_STATUS, FILE_CREATE_DT, REC_CHNG_TMS, PROCESS_NUM, REC_LOAD_TIMESTAMP
)
SELECT
  b.ACCT_NBR,
  b.TKX_DATE,
  b.TKX_TIME,
  b.TKX_NAME,
  b.EMAIL_ADDR1,
  b.EMAIL_ADDR2,
  b.ADDR1,
  b.ADDR2,
  b.CITY,
  b.STATE_PROV,
  b.POSTAL_CODE,
  b.COUNTRY_CODE,
  b.COUNTY_NAME,
  b.TOKEN,
  b.TOKEN_ASSURE_LVL,
  b.TOKEN_REQUEST_ID,
  b.TOKEN_MSG_TYPE,
  b.NBR_ACT_ATTEMPTS,
  b.TOKEN_EXP_DATE,
  b.TOKEN_EVT_RSN_CODE,
  b.TOKEN_EVT_STATUS,
  b.TOKEN_TRANS_IND,
  b.PROVIDER_ID1,
  b.PROVIDER_ID2,
  b.PROVIDER_ID3,
  b.ACCT_ID,
  b.STMT_HOLD_CODE,
