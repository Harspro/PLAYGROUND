  LABELED_DATA AS (
  SELECT
      PCF_CUST_ID,
      PCF_CARD_NUM,
      DEVICE_ID,
      TRANS_ID,
      TRANS_TMS,
      PTS_QTY,
      BONUS_IND,
      TRANS_TY_IND,
      PCF_SERV_CD,
      PCF_SERV_CD_DESC,
      PCF_PRD,
      SOURCE_CD,
      REFERENCE_ID,
      STR_NUM,
      BNS_GRP_NUM,
      CAMPAIGN_NUM,
      CAMPAIGN_DESC,
      REASON_CD,
      REASON_DESC,
      REC_CREATE_TMS,
      REC_LOAD_TMS,
      REC_CHNG_ID,
      REC_CHNG_TMS,
      PROCESSED_FLAG,
      PROCESSED_DATE,
      EXECUTION_ID,
      DIVISION,
      RESPONSE,
      LYLTY_CAMPAIGN_ID,
      LYLTY_TRANS_ID,
      LYLTY_ERR_CD,
      LYLTY_IDENT_ID,
      TRANS_DESC,
      RETRY_NUM,
      ROW_NUMBER() OVER (PARTITION BY TRANS_ID ORDER BY REC_CHNG_TMS DESC) AS REC_RANK
